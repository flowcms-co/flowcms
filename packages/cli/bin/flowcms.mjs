#!/usr/bin/env node
/**
 * Flow CMS setup & operations CLI (zero-dependency — Node built-ins only).
 *
 *   flowcms init [--force] [--dry-run] [--database-url=…] [--admin-email=…] [--admin-password=…]
 *   flowcms secrets            # print freshly generated secrets
 *   flowcms doctor             # check env + prerequisites
 *   flowcms migrate            # apply DB migrations (npm run db:migrate)
 *   flowcms seed               # seed the database (npm run db:seed)
 *   flowcms dev                # start studio + api (npm run dev)
 *   flowcms help
 *
 * Run it from the Flow CMS repo root on the server where the CMS is installed.
 */
import { existsSync, readFileSync, writeFileSync } from "node:fs";
import { join, resolve } from "node:path";
import { randomBytes } from "node:crypto";
import { spawnSync } from "node:child_process";

const C = { reset: "\x1b[0m", dim: "\x1b[2m", green: "\x1b[32m", red: "\x1b[31m", yellow: "\x1b[33m", cyan: "\x1b[36m", bold: "\x1b[1m" };
const ok = (s) => console.log(`${C.green}✓${C.reset} ${s}`);
const bad = (s) => console.log(`${C.red}✗${C.reset} ${s}`);
const warn = (s) => console.log(`${C.yellow}!${C.reset} ${s}`);
const info = (s) => console.log(`${C.cyan}›${C.reset} ${s}`);

const args = process.argv.slice(2);
const cmd = args[0] || "help";
const flag = (name) => {
    const hit = args.find((a) => a === `--${name}` || a.startsWith(`--${name}=`));
    if (!hit) return undefined;
    return hit.includes("=") ? hit.split("=").slice(1).join("=") : true;
};
const root = process.cwd();
const secret = () => randomBytes(32).toString("base64");

function findRepoRoot() {
    // Walk up to a dir that has a package.json with workspaces (the monorepo root).
    let dir = root;
    for (let i = 0; i < 6; i++) {
        const pkg = join(dir, "package.json");
        if (existsSync(pkg)) {
            try {
                const j = JSON.parse(readFileSync(pkg, "utf8"));
                if (j.workspaces || existsSync(join(dir, ".env.example"))) return dir;
            } catch {
                /* ignore */
            }
        }
        const up = resolve(dir, "..");
        if (up === dir) break;
        dir = up;
    }
    return root;
}

function parseEnv(text) {
    const map = {};
    for (const line of text.split(/\r?\n/)) {
        const m = line.match(/^\s*([A-Z0-9_]+)\s*=(.*)$/);
        if (m) map[m[1]] = m[2];
    }
    return map;
}

function cmdSecrets() {
    console.log(`${C.bold}Generated secrets${C.reset} ${C.dim}(each is a fresh random 32-byte base64 string)${C.reset}`);
    console.log(`SECRETS_ENCRYPTION_KEY=${secret()}`);
    console.log(`JWT_SECRET=${secret()}`);
}

function cmdInit() {
    const repo = findRepoRoot();
    const envPath = join(repo, ".env");
    const examplePath = join(repo, ".env.example");
    const dryRun = !!flag("dry-run");
    const force = !!flag("force");

    if (existsSync(envPath) && !force && !dryRun) {
        warn(`.env already exists at ${envPath}. Use --force to overwrite, or --dry-run to preview.`);
        return process.exit(1);
    }

    const example = existsSync(examplePath) ? parseEnv(readFileSync(examplePath, "utf8")) : {};
    const pgPw = secret().replace(/[^a-zA-Z0-9]/g, "").slice(0, 24) || "flowcms";
    const dbUrl =
        flag("database-url") ||
        example.DATABASE_URL ||
        `postgresql://flowcms:${pgPw}@localhost:5432/flowcms?schema=public`;

    // Defaults match the bundled Docker infra; secrets are freshly generated.
    const values = {
        POSTGRES_USER: example.POSTGRES_USER || "flowcms",
        POSTGRES_PASSWORD: example.POSTGRES_PASSWORD || pgPw,
        POSTGRES_DB: example.POSTGRES_DB || "flowcms",
        POSTGRES_PORT: example.POSTGRES_PORT || "5432",
        DATABASE_URL: dbUrl,
        REDIS_PORT: example.REDIS_PORT || "6379",
        REDIS_URL: example.REDIS_URL || "redis://localhost:6379",
        MEILI_PORT: example.MEILI_PORT || "7700",
        MEILI_HOST: example.MEILI_HOST || "http://localhost:7700",
        MEILI_MASTER_KEY: example.MEILI_MASTER_KEY || secret(),
        SECRETS_ENCRYPTION_KEY: secret(),
        JWT_SECRET: secret(),
        API_PORT: example.API_PORT || "4000",
        API_URL: example.API_URL || "http://localhost:4000/api",
        STUDIO_URL: example.STUDIO_URL || "http://localhost:3000",
        NEXT_PUBLIC_API_URL: example.NEXT_PUBLIC_API_URL || "http://localhost:4000/api",
        SEED_ADMIN_EMAIL: flag("admin-email") || example.SEED_ADMIN_EMAIL || "admin@flowcms.local",
        SEED_ADMIN_PASSWORD: flag("admin-password") || secret().replace(/[^a-zA-Z0-9]/g, "").slice(0, 16),
    };

    const body =
        "# Generated by `flowcms init` — keep this file secret (it's gitignored).\n" +
        Object.entries(values)
            .map(([k, v]) => `${k}=${v}`)
            .join("\n") +
        "\n";

    if (dryRun) {
        info("Dry run — this is the .env that would be written:");
        console.log(C.dim + body + C.reset);
        return;
    }

    writeFileSync(envPath, body, { mode: 0o600 });
    ok(`Wrote ${envPath}`);
    console.log(`  ${C.bold}Admin login:${C.reset} ${values.SEED_ADMIN_EMAIL} / ${values.SEED_ADMIN_PASSWORD}`);
    console.log(`  ${C.dim}Secrets (encryption + JWT) were generated. Back up this file.${C.reset}`);
    console.log("\nNext steps:");
    console.log(`  ${C.cyan}npm install${C.reset}`);
    console.log(`  ${C.cyan}npm run infra:up${C.reset}      ${C.dim}# Postgres/Redis/Meilisearch (needs Docker)${C.reset}`);
    console.log(`  ${C.cyan}flowcms migrate${C.reset}        ${C.dim}# apply the schema${C.reset}`);
    console.log(`  ${C.cyan}flowcms seed${C.reset}           ${C.dim}# create the workspace + admin${C.reset}`);
    console.log(`  ${C.cyan}flowcms dev${C.reset}            ${C.dim}# studio :3000 · api :4000${C.reset}`);
}

function cmdDoctor() {
    const repo = findRepoRoot();
    let problems = 0;

    const [maj] = process.versions.node.split(".").map(Number);
    maj >= 18 ? ok(`Node ${process.versions.node}`) : (bad(`Node ${process.versions.node} (need ≥ 18)`), problems++);

    const envPath = join(repo, ".env");
    if (!existsSync(envPath)) {
        bad(`.env not found at ${envPath} — run \`flowcms init\``);
        problems++;
    } else {
        ok(`.env found`);
        const env = parseEnv(readFileSync(envPath, "utf8"));
        for (const key of ["DATABASE_URL", "SECRETS_ENCRYPTION_KEY", "JWT_SECRET"]) {
            env[key] && env[key].trim() ? ok(`${key} set`) : (bad(`${key} missing/empty`), problems++);
        }
        if (env.SECRETS_ENCRYPTION_KEY && env.SECRETS_ENCRYPTION_KEY.length < 16) {
            warn("SECRETS_ENCRYPTION_KEY looks short — use a 32-byte value (`flowcms secrets`).");
        }
    }
    existsSync(join(repo, "node_modules")) ? ok("Dependencies installed") : (warn("node_modules missing — run `npm install`"), 0);

    console.log("");
    problems === 0 ? ok(`${C.bold}All checks passed.${C.reset}`) : bad(`${problems} problem(s) found.`);
    process.exit(problems === 0 ? 0 : 1);
}

function run(npmScript) {
    const repo = findRepoRoot();
    info(`Running: npm run ${npmScript}`);
    const r = spawnSync("npm", ["run", npmScript], { cwd: repo, stdio: "inherit", env: process.env });
    process.exit(r.status ?? 0);
}

function help() {
    console.log(`${C.bold}flowcms${C.reset} — setup & operations for self-hosted Flow CMS

${C.bold}Commands${C.reset}
  init        Scaffold .env (generates secrets, sensible defaults). Flags:
              --force  --dry-run  --database-url=…  --admin-email=…  --admin-password=…
  secrets     Print freshly generated SECRETS_ENCRYPTION_KEY + JWT_SECRET
  doctor      Check Node, .env and required variables
  migrate     Apply database migrations (npm run db:migrate)
  seed        Seed the database (npm run db:seed)
  dev         Start studio + api (npm run dev)
  help        Show this help

Run from the Flow CMS repo root. First time:
  flowcms init  →  npm install  →  npm run infra:up  →  flowcms migrate  →  flowcms seed  →  flowcms dev`);
}

switch (cmd) {
    case "init": cmdInit(); break;
    case "secrets": cmdSecrets(); break;
    case "doctor": cmdDoctor(); break;
    case "migrate": run("db:migrate"); break;
    case "seed": run("db:seed"); break;
    case "dev": run("dev"); break;
    case "help":
    case "--help":
    case "-h": help(); break;
    default:
        bad(`Unknown command "${cmd}".`);
        help();
        process.exit(1);
}
